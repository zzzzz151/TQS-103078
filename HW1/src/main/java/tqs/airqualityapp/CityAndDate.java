package tqs.airqualityapp;

import java.time.LocalDate;

public class CityAndDate {
    private String city;
    private LocalDate date;

    public CityAndDate() {

    }

    public CityAndDate(String city, LocalDate date) {
        this.city = city;
        this.date = date;
    }

    @Override
    public String toString() {
        return "CityAndDate[city=" + city + ", date=" + Utils.dateToStr(date) + "]";
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((city == null) ? 0 : city.hashCode());
        result = prime * result + ((date == null) ? 0 : date.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        CityAndDate other = (CityAndDate) obj;
        if (city == null) {
            if (other.city != null)
                return false;
        } else if (!city.equals(other.city))
            return false;
        if (date == null) {
            if (other.date != null)
                return false;
        } else if (!date.equals(other.date))
            return false;
        return true;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public LocalDate getDate() {
        return date;
    }

    public void setDate(LocalDate date) {
        this.date = date;
    }

}
